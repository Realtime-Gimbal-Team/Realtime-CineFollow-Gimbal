Ssd1306Display::Ssd1306Display(const char* i2c_dev, uint8_t addr)
    : addr_(addr), fb_(1024, 0) {
    fd_ = ::open(i2c_dev, O_RDWR);
    if (fd_ >= 0) {
        ::ioctl(fd_, I2C_SLAVE, addr_);
    }
}

Ssd1306Display::~Ssd1306Display() {
    if (fd_ >= 0) ::close(fd_);
}

bool Ssd1306Display::writeCommand(std::initializer_list<uint8_t> cmds) {
    if (fd_ < 0) return false;

    std::vector<uint8_t> buf;
    buf.reserve(1 + cmds.size());
    buf.push_back(0x00); // control byte for command
    buf.insert(buf.end(), cmds.begin(), cmds.end());

    return ::write(fd_, buf.data(), buf.size()) == (ssize_t)buf.size();
}

bool Ssd1306Display::writeData(const uint8_t* data, size_t len) {
    if (fd_ < 0) return false;

    const size_t chunk = 16;
    for (size_t i = 0; i < len; i += chunk) {
        size_t n = (i + chunk <= len) ? chunk : (len - i);
        uint8_t buf[1 + chunk];
        buf[0] = 0x40; // control byte for data
        std::memcpy(buf + 1, data + i, n);
        if (::write(fd_, buf, 1 + n) != (ssize_t)(1 + n)) return false;
    }
    return true;
}

bool Ssd1306Display::init() {
    if (fd_ < 0) return false;

    bool ok = true;
    ok &= writeCommand({0xAE});                 // Display OFF
    ok &= writeCommand({0xD5, 0x80});           // Set display clock
    ok &= writeCommand({0xA8, 0x3F});           // Multiplex 0x3F (64)
    ok &= writeCommand({0xD3, 0x00});           // Display offset
    ok &= writeCommand({0x40});                 // Start line
    ok &= writeCommand({0x8D, 0x14});           // Charge pump ON
    ok &= writeCommand({0x20, 0x00});           // Memory mode: horizontal
    ok &= writeCommand({0xA1});                 // Segment remap
    ok &= writeCommand({0xC8});                 // COM scan direction
    ok &= writeCommand({0xDA, 0x12});           // COM pins
    ok &= writeCommand({0x81, 0x7F});           // Contrast
    ok &= writeCommand({0xD9, 0xF1});           // Pre-charge
    ok &= writeCommand({0xDB, 0x40});           // Vcom detect
    ok &= writeCommand({0xA4});                 // Resume RAM display
    ok &= writeCommand({0xA6});                 // Normal display
    ok &= writeCommand({0xAF});                 // Display ON

    clear();
    ok &= flush();
    return ok;
}

void Ssd1306Display::clear() {
    std::fill(fb_.begin(), fb_.end(), 0);
}

bool Ssd1306Display::flush() {
    bool ok = true;
    ok &= writeCommand({0x21, 0x00, 0x7F}); // column 0..127
    ok &= writeCommand({0x22, 0x00, 0x07}); // page 0..7
    ok &= writeData(fb_.data(), fb_.size());
    return ok;
}

void Ssd1306Display::setPixel(int x, int y, bool on) {
    if (x < 0 || x >= 128 || y < 0 || y >= 64) return;
    const int page = y / 8;
    const int bit  = y % 8;
    const size_t idx = (size_t)page * 128u + (size_t)x;

    if (on) fb_[idx] |=  (uint8_t)(1u << bit);
    else    fb_[idx] &= (uint8_t)~(1u << bit);
}

void Ssd1306Display::drawText(int x, int y, const std::string& text) {
    int cx = x;

    for (unsigned char ch : text) {
        if (cx >= 128) break;

        if (ch < 32 || ch > 127) ch = ' ';
        const uint8_t* glyph = FONT5X7[ch - 32];

        // 5 columns
        for (int col = 0; col < 5; ++col) {
            uint8_t bits = glyph[col];
            for (int row = 0; row < 7; ++row) {
                bool on = ((bits >> row) & 0x01) != 0;
                setPixel(cx + col, y + row, on);
            }
        }
        // 1 column spacing
        for (int row = 0; row < 7; ++row) {
            setPixel(cx + 5, y + row, false);
        }

        cx += 6;
    }
}